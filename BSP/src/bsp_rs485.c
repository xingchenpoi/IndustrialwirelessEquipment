/*******************************************************************************
** 函数原型：void RS485_Bsp_Init(void)
** 函数功能：RS485初始化
** 输入参数：无
** 输出参数：无
** 备    注：

*******************************************************************************/
void RS485_Bsp_Init(void)
{
	MX_USART1_UART_Init();
	usart_enable(&huart1, s_usart1.RxBuf, USART1_RX_CNT_MAX);
	Usart_Bsp_Init(COM1, sysConfig.uartPara);    //rs485初始化
}



/*******************************************************************************
** 函数原型：uint8_t SPI_ReadWriteByte(uint8_t TxData)
** 函数功能：spi读写一个字节
** 输入参数：hspi    spi头指针
			 TxData  要写入的字节
** 输出参数：读取到的字节
** 备    注：
 *******************************************************************************/
uint8_t SPI_ReadWriteByte(SPI_HandleTypeDef *hspi, uint8_t TxData)
{
	uint8_t Rxdata;
	HAL_SPI_TransmitReceive(hspi, &TxData, &Rxdata, 1, 1000);
	return Rxdata;          		    //返回收到的数据		
}

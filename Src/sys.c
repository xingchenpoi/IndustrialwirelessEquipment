/**
  * @brief  calculate checksum.
  * @param  bPtr the buffer.
  * @param  len buffer len.
  * @retval check sum.
  */
uint8_t checkSum(uint8_t *pch, int32_t len) 
{

	int32_t i;
	uint8_t sum = 0;

	if (pch == NULL || len == 0)
		return 0;

	for (i = 0; i < len; i++)
		sum = sum ^ pch[i];

	return sum;
}

/**
	* @brief  Ascii2BCD
	* @param  byCh: '0' -- 'F'
	* @retval : 0x0--0xF.
	*/
uint8_t ascii2BCD(uint8_t byCh)
{
	if ((byCh >= '0' && byCh <= '9') || \
		(byCh >= 'A' && byCh <= 'F') || \
		(byCh >= 'a' && byCh <= 'f'))
	{
		if (byCh >= 'A')
		{
			if (byCh < 'a')
				return (byCh - 'A' + 0xA);
			else
				return (byCh - 'a' + 0xA);
		}
		else
		{
			return (byCh - '0');
		}
	}

	return 0;
}



/**
  * @brief  BCD2String --- ascii transfer to string.
  * @param  *pDest: 输出字符串
  * @param  *pDataIn: 输入字节数组
  * @param  len: 输入字节数组长度
  * @retval :输出字符串长度，不包括 ‘\0’.
  */
uint32_t BCD2String(uint8_t *pDest, const uint8_t *pDataIn, uint16_t len)
{
	uint8_t byte;
	int strLen = 0;

	while (len)
	{
		byte = (*pDataIn >> 4) & 0x0F;
		*pDest++ = (byte > 9) ? (byte - 0xA + 'A') : (byte + '0');

		byte = *pDataIn & 0x0F;
		*pDest++ = (byte > 9) ? (byte - 0xA + 'A') : (byte + '0');

		pDataIn++;
		len--;

		strLen += 2;
	}

	return strLen;
}



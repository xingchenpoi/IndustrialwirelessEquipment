/*******************************************************************************
** 函数原型：unsigned short CRC_16(unsigned char * aData, unsigned long aSize)
** 函数功能：CRC16计算
** 输入参数：*aData 矫正数据首地址
			 aSize  计算长度
** 输出参数：校验值
** 备    注：
*******************************************************************************/
unsigned short CRC_16(unsigned char * aData, unsigned long aSize)
{
	unsigned long  i;
	unsigned short nAccum = 0;

	for (i = 0; i < aSize; i++)
	{
		nAccum = (nAccum << 8) ^ (unsigned short)Table_CRC[(nAccum >> 8) ^ *aData++];
	}
	return nAccum;
}
